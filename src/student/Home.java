    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        model = (DefaultTableModel) jTable1.getModel();
        rowIndex = jTable1.getSelectedRow();
        tfStudentID.setText(model.getValueAt(rowIndex, 0).toString());
        tfStudentName.setText(model.getValueAt(rowIndex, 1).toString());
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 2).toString());
            jDateOfBirth.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        String gender = model.getValueAt(rowIndex, 3).toString();
        if (gender.equals("Male")) {
            cboGender.setSelectedIndex(0);
        } else {
            cboGender.setSelectedIndex(1);
        }
        tfGmail.setText(model.getValueAt(rowIndex, 4).toString());
        tfPhoneNumber.setText(model.getValueAt(rowIndex, 5).toString());
        tfFatherName.setText(model.getValueAt(rowIndex, 6).toString());
        tfMotherName.setText(model.getValueAt(rowIndex, 7).toString());
        tfAddress1.setText(model.getValueAt(rowIndex, 8).toString());
        tfAddress2.setText(model.getValueAt(rowIndex, 9).toString());
        String path = model.getValueAt(rowIndex, 10).toString();
        imagePath = path;
        lableImage.setIcon(imageAdjust(path, null));
    }//GEN-LAST:event_jTable1MouseClicked

